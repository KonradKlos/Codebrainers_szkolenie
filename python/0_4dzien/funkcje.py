def fibonacci(m):
    #lista = [0,1] # m to numer elementu ciągu który trzeba wyliczyć.
    print(m+1) # na sam koniec wypisuje to m.
    return m+2 # funkcja sie zakańcza i zwraca wartość w tym miejscu.  

wartosc = fibonacci(15) # tak wywołujemy daną funkcję, gdzie program wraca do początku podstawiając (w tym wypadku) liczbę 15 i funkcja się wykonuje.
print(wartosc) #używając return na końcu naszej funkcji zapisuje się ona pod założoną zmienną.